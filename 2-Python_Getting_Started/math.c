#include <stdio.h>
#include <math.h>

int main(){

    printf("sin(1) = %f\n", sin(1));
    printf("exp(10) = %f\n", exp(10));
    printf("log(10) = %f\n", log(10));
    printf("log10(1000) = %f\n", log10(1000));
    printf("log2(1024) = %f\n", log2(1024));
    printf("pow(2, 10) = %f\n", pow(2, 10));

    printf("ceil(3.1415926) = %f\n", ceil(3.1415926));
    printf("floor(3.1415926) = %f\n", floor(3.1415926));

    printf("fabs(23) = %f\n", fabs(23));
    printf("fabs(-23) = %f\n", fabs(-23));

    return 0;

}
